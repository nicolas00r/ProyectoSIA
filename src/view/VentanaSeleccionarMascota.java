    public VentanaSeleccionarMascota(String s, String rutDueño) {
        initComponents();
        this.rutDueño = rutDueño;
        this.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
        ss = s;
        String [] arr = ss.split("\n");
        DefaultTableModel modelM = (DefaultTableModel) tableMascotas.getModel();
        
        if(arr[0].equals("")){
            JOptionPane.showMessageDialog(null, "El cliente no posee mascotas");
            this.rutDueño = "-1";
            return;
        }
        
        for(int i = 0; i < arr.length; i++){
            String[] cc = arr[i].split(", ");
            
            switch (cc.length) {
                case 6:
                    String[] aa = {cc[0], cc[1], cc[3], cc[5], "R"};
                    id = cc[5];
                    modelM.addRow(aa);
                    break;
                case 7:
                    String[] bb = {cc[0], cc[1], cc[4], cc[6], "E"};
                    id = cc[6];
                    modelM.addRow(bb);
                    break;
                default:
                    String[] dd = {cc[0], cc[1], cc[2], cc[4], "N"};
                    id = cc[4];
                    modelM.addRow(dd);
                    break;
            }
        }
    } 

