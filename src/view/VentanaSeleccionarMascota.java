    public String getMascotaSeleccionada() {
        int selectedRow = getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) tableMascotas.getModel();
            return (String) model.getValueAt(selectedRow, 3); 
        }
        return null;
    }
    
    public void actualizarFila(String s){
        int selectedRow = getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tableMascotas.getModel();

        if (selectedRow != -1){
            for(int i = 0; i < 5; i++){
                String[] cc = s.split(", ");

                model.setValueAt(cc[i], selectedRow, i);
            }
        }
    }
    
