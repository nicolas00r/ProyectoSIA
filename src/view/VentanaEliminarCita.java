    public VentanaEliminarCita(String s, String rutDueño, int id) {
        initComponents();
        this.rutDueño = rutDueño;
        this.id = id;
        this.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
        ss = s;
        String [] arr = ss.split("\n");
        DefaultTableModel model = (DefaultTableModel) tableCitas.getModel();
        
        if(arr[0].equals("")){
            JOptionPane.showMessageDialog(null, "El cliente no posee citas");
            this.id = -1;
            return;
        }
       
        for(int i = 0; i < arr.length; i++){
            String[] cc = arr[i].split("¿¿¿");
            model.addRow(cc);
        }
    } 

