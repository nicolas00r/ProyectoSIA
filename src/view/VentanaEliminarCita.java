    public String getCitaSeleccionada() {
        int selectedRow = getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) tableCitas.getModel();
            return (String)model.getValueAt(selectedRow, 4); 
        }
        return null;
    }
    
    public void eliminarFilaSeleccionada() {
        int selectedRow = getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) tableCitas.getModel();
            model.removeRow(selectedRow); // Elimina la fila seleccionada de la tabla
        }
    }
 
    
